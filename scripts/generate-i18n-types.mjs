import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(scriptDir, "..")
const langDir = path.join(repoRoot, "packages/shared/src/locales/langs")
const outputFile = path.join(repoRoot, "packages/shared/src/types/i18n.schema.d.ts")

const locales = ["en-us", "zh-cn"]

function readJson(locale) {
  const filePath = path.join(langDir, `${locale}.json`)
  return JSON.parse(fs.readFileSync(filePath, "utf8"))
}

function isObject(value) {
  return Boolean(value) && typeof value === "object" && !Array.isArray(value)
}

function assertSameShape(base, compare, trail = "root") {
  const baseIsArray = Array.isArray(base)
  const compareIsArray = Array.isArray(compare)
  if (baseIsArray !== compareIsArray) {
    throw new Error(`Shape mismatch at ${trail}: array mismatch`)
  }
  if (isObject(base) !== isObject(compare)) {
    throw new Error(`Shape mismatch at ${trail}: object mismatch`)
  }
  if (isObject(base) && isObject(compare)) {
    const baseKeys = Object.keys(base)
    const compareKeys = Object.keys(compare)
    const baseSet = new Set(baseKeys)
    const compareSet = new Set(compareKeys)
    const missing = baseKeys.filter(key => !compareSet.has(key))
    const extra = compareKeys.filter(key => !baseSet.has(key))
    if (missing.length || extra.length) {
      const details = [
        missing.length ? `missing: ${missing.join(", ")}` : "",
        extra.length ? `extra: ${extra.join(", ")}` : "",
      ]
        .filter(Boolean)
        .join(" | ")
      throw new Error(`Shape mismatch at ${trail}: ${details}`)
    }
    for (const key of baseKeys) {
      assertSameShape(base[key], compare[key], `${trail}.${key}`)
    }
    return
  }
  if (baseIsArray && compareIsArray) {
    if (base.length !== compare.length) {
      throw new Error(`Shape mismatch at ${trail}: array length mismatch`)
    }
    for (let i = 0; i < base.length; i += 1) {
      assertSameShape(base[i], compare[i], `${trail}[${i}]`)
    }
  }
}

function formatKey(key) {
  const isIdentifier = /^[A-Za-z_$][\w$]*$/u.test(key)
  return isIdentifier ? key : JSON.stringify(key)
}

function primitiveType(value) {
  if (value === null)
    return "null"
  if (typeof value === "string")
    return "string"
  if (typeof value === "number")
    return "number"
  if (typeof value === "boolean")
    return "boolean"
  return "unknown"
}

function arrayType(values) {
  if (!values.length)
    return "unknown[]"
  const types = new Set(
    values.map((value) => {
      if (Array.isArray(value))
        return "array"
      if (isObject(value))
        return "object"
      return primitiveType(value)
    }),
  )
  if (types.size !== 1)
    return "unknown[]"
  const onlyType = [...types][0]
  if (onlyType === "array")
    return "unknown[]"
  if (onlyType === "object")
    return "Record<string, unknown>[]"
  return `${onlyType}[]`
}

function renderObject(obj, indent) {
  const lines = []
  for (const [key, value] of Object.entries(obj)) {
    const propKey = formatKey(key)
    if (isObject(value)) {
      lines.push(`${indent}${propKey}: {`)
      lines.push(...renderObject(value, `${indent}  `))
      lines.push(`${indent}}`)
      continue
    }
    if (Array.isArray(value)) {
      lines.push(`${indent}${propKey}: ${arrayType(value)}`)
      continue
    }
    lines.push(`${indent}${propKey}: ${primitiveType(value)}`)
  }
  return lines
}

const localeData = locales.map(locale => ({
  locale,
  data: readJson(locale),
}))

for (let i = 1; i < localeData.length; i += 1) {
  assertSameShape(localeData[0].data, localeData[i].data)
}

const lines = [
  "// This file is auto-generated by scripts/generate-i18n-types.mjs.",
  "// Do not edit by hand.",
  "",
  "declare namespace I18n {",
  "  interface Schema {",
  ...renderObject(localeData[0].data, "    "),
  "  }",
  "}",
  "",
]

fs.writeFileSync(outputFile, lines.join("\n"))
console.log(`Wrote ${path.relative(repoRoot, outputFile)}`)
